public class VariableSet {
    private final String variable;
    private final String value;

    public VariableSet(String variable, String value) {
        this.variable = variable;
        this.value = value;
    }

    public String getVariable() {
        return variable;
    }

    public String getValue() {
        return value;
    }
    
}
